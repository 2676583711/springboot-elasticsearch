package cn.zhou.tools;

public class ForEach {

    //遍历打印整形数组
    public static void output(int[] a) {
        for (int i = 0; i < a.length; i++) {
            System.out.println(a[i]);
        }
    }


    //遍历打印字符数组
    public static void output(char[] a) {
        for (int i = 0; i < a.length; i++) {
            System.out.println(a[i]);
        }
    }
}
